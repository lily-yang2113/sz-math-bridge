# ----- 1.3 绝对值与相反数 -----
def section_1_3():
   lesson = """
## 绝对值与相反数

🧭 **绝对值**：一个数到原点的"距离"，叫这个数的绝对值，用 |a| 表示。

比如苏州园区站到乐桥站的距离是 8 站，不管是去程还是回程，距离都是 8 站。
所以 |+8| = 8，|-8| = 8。

🪞 **相反数**：只有符号不同的两个数互为相反数。

比如：5 和 -5 是相反数，就像苏州东方之门在湖东，它的镜像是湖西。

🌟 **重要规律**：
- 正数的绝对值是它本身：|5| = 5
- 负数的绝对值是它的相反数：|-5| = 5
- 0 的绝对值是 0：|0| = 0
- 互为相反数的两个数绝对值相等
"""
